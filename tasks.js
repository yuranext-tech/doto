function getSetForDay(dayOfWeek) {
    const map = {
        0: SET_C, // Нд
        1: SET_A, // Пн
        2: SET_B, // Вт
        3: SET_C, // Ср
        4: SET_D, // Чт
        5: SET_A, // Пт
        6: SET_B, // Сб
    };
    return map[dayOfWeek] || SET_A;
}

function rand(arr) {
    return arr[Math.floor(Math.random() * arr.length)];
}

// slotDate — 'YYYY-MM-DD' рядок
function pickTask(visitCount, hasPartner, slotDate) {
    const dow = slotDate
        ? new Date(slotDate + 'T12:00:00Z').getDay()
        : new Date().getDay();
    const set = getSetForDay(dow);

    if (!hasPartner) {
        return { type: 'solo', text: rand(set.solo) };
    }

    return {
        type: visitCount <= 1 ? 'absurd' : visitCount <= 4 ? 'ambient' : 'coordination',
        text: rand(set.act2),
        act1: rand(ACT1_SYNC),
        act3: rand(ACT3_CONTACT),
    };
}

function pickAfterglowTogether() { return rand(AFTERGLOW_TOGETHER); }
function pickAfterglowSolo()     { return rand(AFTERGLOW_SOLO); }

